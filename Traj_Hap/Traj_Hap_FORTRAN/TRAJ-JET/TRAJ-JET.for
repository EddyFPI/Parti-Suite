      SUBROUTINE FORCEDRAG (FUN2,FUN3,FUN4,M3,VZ,VR,FDRGR,FDRGZ)
      
      DOUBLE PRECISION FUN2,FUN3,FUN4,M3,VZ,VR,FDRGR,FDRGZ
      
